    private void fillTable(String last, String init){
        
        Server server  = Server.getInstance();
        String query="";
        if(last!=""){
            
            query="SELECT idpay,service,datepay,tipepay,amount FROM pay WHERE datepay>=\'"+init+"\' AND datepay<=\'"+last+"\' ORDER BY datepay DESC;";
            System.out.println(query);
        
        }else{
            query="SELECT idpay,service,datepay,tipepay,amount FROM pay ORDER BY datepay DESC;";
        
        }
        
        
        ResultSet result = server.getResult(query);
        
        try{
            while(result.next()){
                String[] row = new String[5];
                
                
                row[0] = result.getString(1);
                row[1] = result.getString(2);
                row[2] = result.getString(3);
                row[3] = result.getString(4)=="1"? "deposito":"contado";//operador ternario
                row[4] = result.getString(5);
                this.model.addRow(row);
            }
        }catch(SQLException ex){
        }
    
    }
    private String convertDate(Date date){
        String dateResult="";
        int year= date.getYear()+1900;
        int month = date.getMonth()+1;
        int day = date.getDate();
        System.out.println("AÃ‘O: "+year);
        System.out.println("MES: "+month);
        System.out.println("DIA: "+day);
        dateResult=year+"-"+month+"-"+day;
        return dateResult;
    }
